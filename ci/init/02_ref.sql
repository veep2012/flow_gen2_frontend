    ON ref.permissions(user_id, project_id)
    WHERE discipline_id IS NULL;

CREATE UNIQUE INDEX uq_permissions_user_anyproj_disc
    ON ref.permissions(user_id, discipline_id)
    WHERE project_id IS NULL;

CREATE UNIQUE INDEX uq_permissions_user_anyscope
    ON ref.permissions(user_id)
    WHERE project_id IS NULL AND discipline_id IS NULL;

