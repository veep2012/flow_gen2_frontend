);

CREATE TABLE user_roles (
    user_id SMALLINT NOT NULL REFERENCES ref.users(user_id) ON DELETE CASCADE,
    role_id SMALLINT NOT NULL REFERENCES ref.roles(role_id) ON DELETE CASCADE,
    PRIMARY KEY (user_id, role_id)
);

CREATE TABLE role_permissions (
    role_id SMALLINT NOT NULL REFERENCES ref.roles(role_id) ON DELETE CASCADE,
    resource VARCHAR(32) NOT NULL,
    capability VARCHAR(16) NOT NULL,
    CONSTRAINT pk_role_permissions PRIMARY KEY (role_id, resource, capability),
    CONSTRAINT chk_role_permissions_resource CHECK (
        resource IN ('doc', 'doc_revision', 'files', 'files_commented')
    ),
    CONSTRAINT chk_role_permissions_capability CHECK (
        capability IN ('read-only', 'read-write')
    )
);

CREATE TABLE role_scopes (
    role_id SMALLINT NOT NULL REFERENCES ref.roles(role_id) ON DELETE CASCADE,
    scope_type VARCHAR(16) NOT NULL,
    entity_id INTEGER NOT NULL,
    logic_group INTEGER NOT NULL DEFAULT 1,
    CONSTRAINT pk_role_scopes PRIMARY KEY (role_id, scope_type, entity_id, logic_group),
    CONSTRAINT chk_role_scopes_scope_type CHECK (
        scope_type IN ('PROJECT', 'AREA', 'UNIT')
    ),
    CONSTRAINT chk_role_scopes_logic_group_positive CHECK (logic_group > 0)
);

CREATE INDEX ix_roles_role_code ON ref.roles(role_code);
