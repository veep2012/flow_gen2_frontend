CREATE TABLE users (
    user_id SMALLINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id SMALLINT NOT NULL REFERENCES ref.person(person_id),
    user_acronym VARCHAR(10) NOT NULL,
    role_id SMALLINT NOT NULL REFERENCES ref.roles(role_id),
