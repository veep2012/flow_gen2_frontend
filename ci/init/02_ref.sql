CREATE OR REPLACE FUNCTION ref.sync_user_primary_role()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ref, pg_temp
AS $$
BEGIN
    IF NEW.role_id IS NULL THEN
        RETURN NEW;
    END IF;

    -- Legacy compatibility sync: keep users.role_id mirrored into user_roles,
    -- but do not remove existing secondary role assignments.
    INSERT INTO ref.user_roles (user_id, role_id)
    VALUES (NEW.user_id, NEW.role_id)
    ON CONFLICT (user_id, role_id) DO NOTHING;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_sync_user_primary_role
AFTER INSERT OR UPDATE OF role_id
ON ref.users
FOR EACH ROW
EXECUTE FUNCTION ref.sync_user_primary_role();

